package ch02;

public class RealTypeEx {

	public static void main(String[] args) {
		double d = 0.1234;
		double e = 1234E-4;
		float f = 0.1234f; //float은 숫자 뒤에 f,F 붙여야함
		double w = .1234;
		
		System.out.println(d);
		System.out.println(e);	
		System.out.println(f);
		System.out.println(w);
	}
}
